// scripts/bin/sync-docs-index.ts — regenerate docs/INDEX.md from the filesystem.
//
// Performance Contract:
//   cold_start  < 300 ms
//   wall_time   < 5 s    (filesystem walk)
//   mem_peak    < 64 MB

import { existsSync, readFileSync, readdirSync, statSync, writeFileSync } from 'node:fs';
import { basename, join, relative } from 'node:path';

import { nxThrow } from '../lib/errors';
import { defineScript } from '../lib/skeleton';

export const meta = {
  name: 'sync-docs-index',
  version: '0.1.0',
  description: 'Regenerate docs/INDEX.md from the filesystem.',
  flags: {
    check: 'switch',
    env: 'string',
  },
  exitCodes: [
    { code: 0, meaning: 'written or up to date' },
    { code: 8, meaning: 'drift detected (--check mode)' },
  ],
} as const;

function listDirs(root: string): string[] {
  const out: string[] = [root];
  const stack: string[] = [root];
  while (stack.length > 0) {
    const cur = stack.pop();
    if (!cur) continue;
    let entries: string[];
    try {
      entries = readdirSync(cur);
    } catch {
      continue;
    }
    for (const name of entries) {
      const full = join(cur, name);
      try {
        if (statSync(full).isDirectory()) {
          out.push(full);
          stack.push(full);
        }
      } catch {
        /* skip */
      }
    }
  }
  return out.sort();
}

function listMdFiles(dir: string): string[] {
  let entries: string[];
  try {
    entries = readdirSync(dir);
  } catch {
    return [];
  }
  return entries
    .filter((n) => n.endsWith('.md') && n !== 'INDEX.md')
    .filter((n) => {
      try {
        return statSync(join(dir, n)).isFile();
      } catch {
        return false;
      }
    })
    .sort();
}

function firstHeading(file: string): string {
  try {
    const head = readFileSync(file, 'utf8').split(/\r?\n/).slice(0, 5);
    for (const line of head) {
      const m = line.match(/^#\s+(.*)$/);
      if (m?.[1]) return m[1];
    }
  } catch {
    /* fall through */
  }
  return basename(file);
}

function buildIndex(docs: string): string {
  const lines: string[] = [];
  lines.push('<!-- SPDX-License-Identifier: MIT -->');
  lines.push('<!-- Copyright (c) 2026 Nexus Engine contributors -->');
  lines.push('<!-- AUTO-GENERATED by scripts/sync-docs-index. DO NOT EDIT BY HAND. -->');
  lines.push('');
  lines.push('# Nexus Engine — Documentation Index');
  lines.push('');
  lines.push(`Generated: ${new Date().toISOString().replace(/\.\d{3}Z$/, 'Z')}`);
  lines.push('');
  for (const dir of listDirs(docs)) {
    const rel = relative(docs, dir) || '(root)';
    lines.push(`## ${rel}`);
    lines.push('');
    for (const f of listMdFiles(dir)) {
      const title = firstHeading(join(dir, f));
      lines.push(`- [${title}](${rel}/${f})`);
    }
    lines.push('');
  }
  return lines.join('\n');
}

function stripGenerated(text: string): string {
  return text
    .split(/\r?\n/)
    .filter((l) => !l.startsWith('Generated:'))
    .join('\n');
}

await defineScript(meta, async (args) => {
  const root = process.env.NEXUS_ROOT ?? process.cwd();
  const docs = `${root}/docs`;
  const indexPath = `${docs}/INDEX.md`;

  if (args['dry-run']) {
    return { exitCode: 0, summary: { would_write: indexPath } };
  }

  const newIndex = buildIndex(docs);

  if (args.check) {
    const current = existsSync(indexPath) ? readFileSync(indexPath, 'utf8') : '';
    if (existsSync(indexPath) && stripGenerated(current) === stripGenerated(newIndex)) {
      return { exitCode: 0, summary: { drift: false } };
    }
    nxThrow({
      tag: 'Partial',
      message: 'docs/INDEX.md drifted',
      location: indexPath,
      suggestedFix: 'scripts/sync-docs-index',
    });
  }

  writeFileSync(indexPath, newIndex);
  return { exitCode: 0, summary: { written: indexPath } };
});
